     */
    public function store(Request $request)
    {

        $group_id = array($request->sender_id , $request->receiver_id);
         sort($group_id);
        $group = $group_id[0]."_".$group_id[1];
       // return response()->json($group);
        $message = new Message();
        $message->sender_id = $request->sender_id ;
        $message->receiver_id = $request->receiver_id;
        $message->message = $request->message ;
        $message->group = $group;
        $message->save();

        return response()->json('Mesaj Kayıt Yapıldı.');

    }

